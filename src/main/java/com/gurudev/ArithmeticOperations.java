package com.gurudev;

public class ArithmeticOperations {

    public double calculateDivision(double a, double b) {
        return a / b;
    }

    public double calculateMultiplication(double a, double b) {
        return a * b;
    }

    public double calculateAddition(double a, double b) {
        return a + b;
    }

    public double calculateSubtraction(double a, double b) {
        return a - b;
    }

    public double calculateModulus(double a, double b) {
        return a % b;
    }
}
